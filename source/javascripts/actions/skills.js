const UPDATE_SKILLS = 'UPDATE_SKILLS';
const CLEAR_SKILLS = 'CLEAR_SKILLS';


// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

export function updateSkills(skills) {
  return {
    type: UPDATE_SKILLS,
    skills
  };
}

// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

export function clearSkills() {
  return {
    type: CLEAR_SKILLS
  };
}

